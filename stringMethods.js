let myName = "prakhar";

//slice
let slicedName = myName.slice(0, 3);
console.log(slicedName);

//replace
let replacedName = myName.replace("pra", "PRA");
console.log(replacedName);

//concat
let fullName = myName.concat(" ", "sharma");
console.log(fullName);

//trim
let trimmedName = myName.trim();
console.log(trimmedName);
